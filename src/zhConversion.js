function reverseMap(map) {
    const reversed = {};
    Object.entries(map).forEach(([key, value]) => {
        if (!Object.prototype.hasOwnProperty.call(reversed, value)) reversed[value] = key;
    });
    return reversed;
}

const T2S_PHRASES = reverseMap(S2T_PHRASES);
const T2S_CHARS = reverseMap(S2T_CHARS);
const S2T_PHRASE_ENTRIES = Object.entries(S2T_PHRASES).sort((a, b) => b[0].length - a[0].length);
const T2S_PHRASE_ENTRIES = Object.entries(T2S_PHRASES).sort((a, b) => b[0].length - a[0].length);

function uniqueValues(values) {
    const seen = new Set();
    const result = [];
    values.forEach((value) => {
        const normalized = String(value ?? '');
        if (!normalized || seen.has(normalized)) return;
        seen.add(normalized);
        result.push(normalized);
    });
    return result;
}

function escapeRegExpLiteral(value) {
    return String(value ?? '').replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function escapeRegExpCharClassValue(value) {
    return String(value ?? '')
        .replace(/\\/g, '\\\\')
        .replace(/\]/g, '\\]')
        .replace(/\^/g, '\\^')
        .replace(/-/g, '\\-');
}

export function getChineseCharVariants(char) {
    const source = String(char ?? '');
    if (!source) return [];
    return uniqueValues([
        source,
        S2T_CHARS[source],
        T2S_CHARS[source],
    ]);
}

export function buildChineseVariantPattern(value) {
    return Array.from(String(value ?? '')).map((char) => {
        const variants = getChineseCharVariants(char);
        if (variants.length <= 1) return escapeRegExpLiteral(char);
        return `[${variants.map(escapeRegExpCharClassValue).join('')}]`;
    }).join('');
}

export function convertChineseText(value, direction) {
    if (typeof value !== 'string' || value.length === 0) return value;
    const phraseEntries = direction === 't2s' ? T2S_PHRASE_ENTRIES : S2T_PHRASE_ENTRIES;
    const charMap = direction === 't2s' ? T2S_CHARS : S2T_CHARS;
    let output = '';
    let cursor = 0;

    while (cursor < value.length) {
        let matched = null;
        for (const entry of phraseEntries) {
            if (value.startsWith(entry[0], cursor)) {
                matched = entry;
                break;
            }
        }
        if (matched) {
            output += matched[1];
            cursor += matched[0].length;
            continue;
        }
        const char = value[cursor];
        output += charMap[char] || char;
        cursor++;
    }

    return output;
}

function convertStringArray(values, direction) {
    return Array.isArray(values) ? values.map((value) => convertChineseText(String(value ?? ''), direction)) : [];
}

function convertOptionalString(value, direction) {
    return typeof value === 'string' ? convertChineseText(value, direction) : value;
}

export function convertRuleListChinese(rules, direction) {
    return (Array.isArray(rules) ? rules : []).map((rule) => {
        const nextRule = { ...(rule || {}) };
        nextRule.name = convertOptionalString(nextRule.name, direction);
        nextRule.subRules = (Array.isArray(nextRule.subRules) ? nextRule.subRules : []).map((subRule) => ({
            ...(subRule || {}),
            targets: convertStringArray(subRule?.targets, direction),
            replacements: convertStringArray(subRule?.replacements, direction),
            remark: convertOptionalString(subRule?.remark, direction),
        }));
        return nextRule;
    });
}
